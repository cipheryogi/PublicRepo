# Parentheses (): Expressions within parentheses are evaluated first.

# Exponentiation **: Exponentiation is performed before any other binary operators.

# Multiplication *, Division /, Integer Division //, Modulus %: These arithmetic operations have the same precedence and are evaluated from left to right.

# Addition + and Subtraction -: These arithmetic operations have the same precedence and are evaluated from left to right.

# Bitwise Shifts << (left shift) and >> (right shift): These operators have lower precedence than addition and subtraction.

# Bitwise AND &, Bitwise OR |, Bitwise XOR ^: These bitwise operations have lower precedence than shifts.

# Comparison Operators: These include <, >, <=, >=, ==, and !=. They are used for comparing values and have lower precedence than the bitwise operators.

# Logical NOT not: The logical NOT operator is used for negation.

# Logical AND and: The logical AND operator is used for conjunction.

# Logical OR or: The logical OR operator is used for disjunction.

print(1 ** 6 // 3) # output is 0